struct Queue {
    private var arr: [Int] = []
    private var index: Int = 0
    
    var size: Int {
        return arr.count - index
    }
    
    var isEmpty: Bool {
        return arr.isEmpty
    }
    
    var last: Int {
        return arr.last ?? -1
    }
    
    mutating func push(_ n: Int ) {
        arr.append(n)
    }
    
    @discardableResult
    mutating func pop() -> Int {
        if arr.isEmpty {
            return -1
        } else {
            let tmp: Int = arr[index]
            index += 1
            return tmp
        }
    }
}
let n = Int(readLine()!)!
var queue = Queue()

for i in 1...n {
    queue.push(i)
}

while queue.size != 1 {
    queue.pop()
    queue.push(queue.pop())
}

print(queue.last)
